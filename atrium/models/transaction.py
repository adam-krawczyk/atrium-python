import json

class Transaction:
    account_guid = None
    category = None
    check_number = None
    created_at = None
    date = None
    description = None
    guid = None
    is_bill_pay = None
    is_direct_deposit = None
    is_expense = None
    is_fee = None
    is_income = None
    is_overdraft_fee = None
    is_payroll_advance = None
    latitude = None
    longitude = None
    member_guid = None
    memo = None
    merchant_category_code = None
    original_description = None
    posted_at = None
    status = None
    top_level_category = None
    transacted_at = None
    type = None
    updated_at = None
    user_guid = None

    def __init__(self, response):
        parsedJSON = json.loads(response)
        self.account_guid = parsedJSON["account_guid"]
        self.amount = parsedJSON["amount"]
        self.category = parsedJSON["category"]
        self.check_number = parsedJSON["check_number"]
        self.created_at = parsedJSON["created_at"]
        self.date = parsedJSON["date"]
        self.description = parsedJSON["description"]
        self.guid = parsedJSON["guid"]
        self.is_bill_pay = parsedJSON["is_bill_pay"]
        self.is_direct_deposit = parsedJSON["is_direct_deposit"]
        self.is_expense = parsedJSON["is_expense"]
        self.is_fee = parsedJSON["is_fee"]
        self.is_income = parsedJSON["is_income"]
        self.is_overdraft_fee = parsedJSON["is_overdraft_fee"]
        self.is_payroll_advance = parsedJSON["is_payroll_advance"]
        self.latitude = parsedJSON["latitude"]
        self.longitude = parsedJSON["longitude"]
        self.member_guid = parsedJSON["member_guid"]
        self.memo = parsedJSON["memo"]
        self.merchant_category_code = parsedJSON["merchant_category_code"]
        self.original_description = parsedJSON["original_description"]
        self.posted_at = parsedJSON["posted_at"]
        self.status = parsedJSON["status"]
        self.top_level_category = parsedJSON["top_level_category"]
        self.transacted_at = parsedJSON["transacted_at"]
        self.type = parsedJSON["type"]
        self.updated_at = parsedJSON["updated_at"]
        self.user_guid = parsedJSON["user_guid"]
